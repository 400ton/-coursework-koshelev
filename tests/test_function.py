def test_load_file():
    '''Функция тестирования загрузки файлов'''

    data_file = '../data/test_data.json'
    with open(data_file, 'r', encoding='utf-8') as file:
        json.load(file)

    # Проверяем, что функция корректно загружает данные из файла
    expected_data = [{"key": "value"}]
    actual_data = load_file(data_file)
    assert actual_data == expected_data